package estruturaDados1.aula6;

public class Rota {

    private String nome;
    private Double distancia;


    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Double getTamanho() {
        return distancia;
    }

    public void setTamanho(Double distancia) {
        this.distancia = distancia;
    }
}
